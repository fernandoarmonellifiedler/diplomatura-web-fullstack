/* Instalación
Para instalar React previamente debemos tener instalado NodeJS. En caso que no tengas
NodeJS instalado, lo podés descargar desde la página oficial de NodeJS.

Una vez que nos aseguramos de tener NodeJS instalado, debemos abrir una
terminal/consola en nuestra computadora (Windows símbolo de sistema, MAC y Linux una
terminal). Ya en la terminal, debemos escribir el siguiente comando

npm install -g create-react-app

El cual instalará una aplicación que nos permite crear un nuevo proyecto en React. Esta
operación puede demorar unos minutos y necesita de una conexión a internet.

------------------------------
Línea de comandos de React

Una vez instalada la aplicación de generación de proyectos en React (create-react-app), y
manteniéndonos en la consola, debemos dirigirnos a la carpeta de nuestra computadora
en al cual deseamos crear el proyecto React. Y en dicha carpeta escribir

create-react-app <nombre de la aplicación>

Reemplazaremos <nombre de la aplicación> por el nombre que deseemos asignarle a
nuestro proyecto. Esta operación puede demorar unos minutos.

Ejemplo

create-react-app miPrimerProyecto

Ingresamos a la carpeta del proyecto recién generado

cd <nombre de la aplicación>

Y por último podemos ver el proyecto en funcionamiento con la siguiente línea en la
consola

npm start

Con esta simple línea de comando se abrirá un navegador y podremos ver la aplicación
inicial en funcionamiento.
Cada vez que deseemos ver el proyecto, debemos ejecutar la sentencia npm start para iniciar el proyecto. Mientras el proyecto se encuentra corriendo, actualiza
automáticamente en el navegador, todos los cambios que realicemos en el código fuente.
Esta forma de trabajo es muy útil para poder ver en el momento el impacto de nuestros
cambios.
*/