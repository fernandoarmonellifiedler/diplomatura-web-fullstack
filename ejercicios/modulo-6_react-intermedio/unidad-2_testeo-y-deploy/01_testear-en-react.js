/* Formas de testear React

Para testear en React podemos utilizar la biblioteca cypress.

Una de las ventajas de cypress es que ofrece una interfaz gráfica para el testeo y además permite testear también aplicaciones móviles.

----------------------------------
Instalación
Dentro de la carpeta del proyecto, realizar la instalación con npm.

$ npm install cypress --save-dev

Hay otras vías para la instalación y pueden ser consultadas en la página de cypress.

----------------------------------
Ejecución
Para ejecutar cypress desde línea de comandos:

$ npx cypress open

Con lo que se abrirá la aplicación cypress
*/

/* Realizar un testeo
Para crear un nuevo test
1. Crear un archivo nuevo dentro de la carpeta cypress/integration/examples que se encuentra dentro de tu proyecto y escribí el código de testeo
2. Corroborar que el cambio se ve reflejado en la aplicación de cypress
3. Corre el testeo desde la aplicación cypress

En cuanto al código de testeo, en la página de cypress se cuenta con documentación y
varios ejemplos a tal fin:

https://docs.cypress.io/guides/getting-started/writing-your-first-test.html#Step-1-Visit-a-page
*/